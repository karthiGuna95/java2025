package forLoop;

public class ForInteger {
	public static void main(String[] args) {
		for (int i = 10 ; i >= 1 ; i--) {
			System.out.println(i);
		}
		
		for (int i = 50 ; i <= 100 ; i++) {
			System.out.println(i);
		}
		for (int i = 100 ; i >= 50 ; i--) {
			System.out.println(i);
		}
	}
}
