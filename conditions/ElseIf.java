package conditions;

public class ElseIf {
	public static void main (String[] args) {
		int num = -25;
		if (num > 0) {
			System.out.println("Positive");
		}
		else if (num == 0) {
			System.out.println("Neutral");
		}
		else {
			System.out.println("Negative");
		}
			
		
	}

}
